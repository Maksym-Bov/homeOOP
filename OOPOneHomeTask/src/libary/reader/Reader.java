package libary.reader;

public class Reader {

    private String firstName;
    private String lastName;
    private String middleName;
    private int numberReader;
    private String faculty;
    private String birthdayReader;
    private int phoneNumberReader;


    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public int getNumberReader() {
        return numberReader;
    }

    public void setNumberReader(int numberReader) {
        this.numberReader = numberReader;
    }

    public String getFaculty() {
        return faculty;
    }

    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    public String getBirthdayReader() {
        return birthdayReader;
    }

    public void setBirthdayReader(String birthdayReader) {
        this.birthdayReader = birthdayReader;
    }

    public int getPhoneNumberReader() {
        return phoneNumberReader;
    }

    public void setPhoneNumberReader(int phoneNumberReader) {
        this.phoneNumberReader = phoneNumberReader;
    }
}
