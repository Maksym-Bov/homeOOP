package libary;

public class Reader {

    private String firstName;
    private String lastName;
    private String middleName;
    private int numberReader;
    private String faculty;
    private String birthdayReader;
    private int phoneNumberReader;

    public Reader(String firstName, String lastName, String middleName, int numberReader, String faculty, String birthdayReader, int phoneNumberReader) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.middleName = middleName;
        this.numberReader = numberReader;
        this.faculty = faculty;
        this.birthdayReader = birthdayReader;
        this.phoneNumberReader = phoneNumberReader;
    }
}
